/**
 * op_add - addition function
 * @a: first int
 * @b: second
 * Return: result
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - sub function
 * @a: first int
 * @b: second
 * Return: result
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**     
 * op_mul - multiply function
 * @a: first int
 * @b: second
 * Return: result
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**     
 * op_div - divide function
 * @a: first int
 * @b: second
 * Return: result
 */
int op_div(int a, int b)
{
return (a / b);
}
/**     
 * op_mod - mod function
 * @a: first int
 * @b: second
 * Return: result
 */
int op_mod(int a, int b)
{
return (a % b);
}
